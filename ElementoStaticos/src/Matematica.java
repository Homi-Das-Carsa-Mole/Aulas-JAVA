
public class Matematica {
	
	//Métodos "somar" e "subtrair", ambos estáticos e que, para funcionarem, dependem apenas da inserção de dados do tipo "int" em seus parâmetros:
	
	public static int somar(int a, int b) {
		return a + b;
	}
	
	public static int subtrair(int a, int b) {
		return a - b;
	}
}
